TOKEN_RE = re.compile(r"(\{\{[^}]+\}\}|«[^»]+»|\[[^\]]+\]|/mo|/ 5 MAX|€[\d,]+|\d+\.\d{3} €|FDF Today|Champions League|Europa League|DD Recommends|FDF|MAX LEVEL|\.\.\.)")


def _protect(text: str) -> tuple[str, list[str]]:
    tokens: list[str] = []

    def repl(m: re.Match[str]) -> str:
        tokens.append(m.group(0))
        return f"\x00{len(tokens) - 1}\x00"

    return TOKEN_RE.sub(repl, text), tokens


def _restore(text: str, tokens: list[str]) -> str:
    for i, tok in enumerate(tokens):
        text = text.replace(f"\x00{i}\x00", tok)
    return text


def _apply_glossary(text: str, glossary: dict[str, str]) -> str:
    for src, dst in sorted(glossary.items(), key=lambda x: -len(x[0])):
        text = text.replace(src, dst)
    return text


def _apply_rules(text: str, rules: list[tuple[str, str]]) -> str:
    for src, dst in rules:
        if text.startswith(src):
            return dst + text[len(src) :]
        if src in text:
            text = text.replace(src, dst)
    return text


